export function loadSurfaceThemeId(){
  try{ return String(localStorage.getItem(LS_SURFACE_THEME)||""); }catch{ return ""; }
}
export function saveSurfaceThemeId(id){
  try{ localStorage.setItem(LS_SURFACE_THEME, String(id||"")); }catch{}
}
export function applySurfaceThemeById(id){
  const pick = (SURFACE_THEMES_200||[]).find(t=>t && t.id===id) || (SURFACE_THEMES_200||[])[0];
  if (!pick) return;
  const light = (document.documentElement.getAttribute("data-theme")==="light");
  const d = light ? (pick.light||{}) : (pick.dark||{});
  const root = document.documentElement.style;
  if (d.fxScrollFill) root.setProperty("--fx-scroll-fill", d.fxScrollFill);
  if (d.favActualBg) root.setProperty("--fav-actual-bg", d.favActualBg);
  if (d.favAddTitleBg) root.setProperty("--fav-add-title-bg", d.favAddTitleBg);
  if (d.bgMain) root.setProperty("--color-bg-main", d.bgMain);
  if (d.bgStage) root.setProperty("--color-bg-stage", d.bgStage);
  if (d.bgChip) root.setProperty("--color-bg-chip", d.bgChip);
  if (d.line) root.setProperty("--color-line", d.line);
  saveSurfaceThemeId(pick.id);
}
